#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*string;

	i = ft_strlen(s1);
	string = malloc(i + 1);
	if (string == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		string[i] = s1[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
